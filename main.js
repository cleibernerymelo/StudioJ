// DOM --> Document Object Model - Modelção do documento Html em objecto Javascript.

/* Abre e fecha o menu quando clica no icone de menu*/
const nav = document.querySelector('#header nav')
const toggle = document.querySelectorAll('nav .toggle')

/* 
Existem varios elementos toggle no html, com o laço consigo passar por todos
Explicando o codigo abaixo.
addEventListener --> Estou ouvindo
Click --> Cliquei, o que vc quer que eu faça
Function -- > Execute a função e chame o alert
*/
for (const element of toggle) {
  element.addEventListener('click', function () {
    nav.classList.toggle('show')
  })
}

/* Quando clicar em um item do menu, esconder o menu*/

const links = document.querySelectorAll('nav ul li a')
/*Expicando o laço abaixo: para cada link dos links execute a instrução*/
for (const link of links) {
  link.addEventListener('click', function () {
    nav.classList.remove('show')
  })
}

